package PST2;

public class Class2method {

	public static void main(String[] args) {
	flower.lilly();
	fruit.banana();
	food.biriyani();

	}
}

class flower {
	public static void lilly () {
		System.out.println("lilly is a flower");
	}
   }
class fruit {
	public static void banana() {
		System.out.println("banana is a fruit");
	}
}

class food{
	public static void biriyani() {
		System.out.println("biriyani is a food ");
	}
}
    
   
